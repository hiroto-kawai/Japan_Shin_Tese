export function getPolicyEvaluation(partyId, policyId) {
  // partyPolicyScoresからスコアを直接取得
  const score = partyPolicyScores[partyId]?.[policyId];
  
  if (score === undefined) {
    return {
      score: null,
      explanation: "この政策に関する評価情報がありません。"
    };
  }
  
  // partyPolicyEvaluationsから該当する評価情報を直接取得
  const policyEvaluation = partyPolicyEvaluations[partyId]?.[policyId] || {};
  
  // policyIssuesから政策名を取得
  const policyObj = policyIssues.find(p => p.id === policyId);
  const policyName = policyObj?.name || "";
  
  // トレードオフ分析情報を取得
  // まず特定の政党・政策の組み合わせで定義されたtradeoffs情報を使用
  let tradeoffs = policyEvaluation.tradeoffs || [];
  let bestApproach = policyEvaluation.bestApproach || "";
  
  // 特定の組み合わせで定義されていなければ、共通のtradeoffAnalysisから取得
  if (tradeoffs.length === 0) {
    const tradeoff = tradeoffAnalysis[policyId] || {};
    tradeoffs = tradeoff.tradeoffs || [];
    bestApproach = tradeoff.bestApproach || "";
  }
  
  // 政策分野の横断的評価情報を取得
  const areaEvaluation = policyAreaEvaluations[policyId] || {};
  
  // valueIntegrationを取得
  // まず特定の政党・政策の組み合わせで定義されたvalueIntegration情報を使用
  const valueIntegration = policyEvaluation.valueIntegration || "";
  
  // 返却情報の構築
  return {
    score,
    explanation: policyEvaluation.explanation || "",
    policyName,
    tradeoffs,
    bestApproach,
    areaAnalysis: areaEvaluation.analysis || "",
    partyIsBest: areaEvaluation.bestParty === partyId,
    valueIntegration
  };
}

