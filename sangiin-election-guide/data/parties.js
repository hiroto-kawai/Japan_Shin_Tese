// 重み付けスコア計算
const calculateWeightedScore = (partyId) => {
  let totalScore = 0;
  let totalWeight = 0;
  
  Object.entries(partyPolicyScores[partyId]).forEach(([issueId, score]) => {
    const issue = policyIssues.find(i => i.id === issueId);
    if (issue) {
      const weight = issue.weight || 1;
      totalScore += score * weight;
      totalWeight += weight;
    } else {
      totalScore += score;
      totalWeight += 1;
    }
  });
  
  return totalWeight > 0 ? (totalScore / totalWeight).toFixed(1) : 0;
};

